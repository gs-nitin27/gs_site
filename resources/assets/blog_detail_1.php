<p>Due to lack of sponsorship or apathy of the government or federations. Have you observed that most of our athletes who make the part of an Olympic contingent are generally from lower or lower middle class background? While, I have nothing against these people but one question which needed to be answered is ‘Why India is represented by people from the strata of the society which barely have access to good sports facilities, top class equipments, nutritional food and other important things required to be an elite athlete. 
</p>
<p>
The obvious reason might be ‘sports talent’. However, I do not believe that India’s rich and affluent and strong middle have paucity of talent. Even sports which are historically associated with rich and affluent (Tennis & golf) find very little representation of the elite in International competition. (Most of the golf players are former caddies). For the sake of argument, let's look at few pointers. Most of the elite sports facilities are used by them, in fact they they use facilities in other countries when they travel abroad.They buy the best sports equipments. They are the most fittest people in India. Their eating habits are becoming nutrition friendly. 
</p>
<p>
If we look at all the private schools (where most of affluent kids study) in India, then we find that majority of them have decent sports infrastructure. In fact, many of them have infra which rival the world’s best. 
</p>
<p>
So, still we get so few people in elite sports from this strata. One can say the reason may be politics in sports but we are not dealing with politics today. We want the actual reason.
</p>
